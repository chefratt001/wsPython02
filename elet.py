
# คำนวณพลังงานไฟฟ้าในหน่วยเครื่องปรับอากาศต่อวัน
h = 240
P_1 = int(input("แอร์เครื่องที่ 1 : "))
P_2 = int(input("แอร์เครื่องที่ 2 : "))
P_3 = int(input("แอร์เครื่องที่ 3 : "))

cost = float(input("ค่าไฟยูนิตละ : "))

U1 = (P_1* h)/1000
U2 = (P_2* h)/1000
U3 = (P_3* h)/1000

total = (U1+U2+U3)*cost

print("ราคาไฟบ้านทั้งหมด",total)




